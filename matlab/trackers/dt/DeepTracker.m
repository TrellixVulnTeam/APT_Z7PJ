      obj.updateTrackerInfo();
      
      res = questdlg(sprintf('Training stopped after %d / %d iterations. Save trained model to file?',...
        obj.trackerInfo.iterCurr,obj.trackerInfo.iterFinal),'Save?','Save','Save as...','No','Save');
